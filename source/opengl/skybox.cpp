void skybox::render(int cubemap,glm::mat4 projection,glm::mat4 view) {
    graphics::shaders.at(8)->activate();
    graphics::shaders.at(8)->setVector("projection", glm::value_ptr(projection));
    graphics::shaders.at(8)->setVector("view", glm::value_ptr(view));
    // skybox cube
    glBindVertexArray(VAO);
    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_CUBE_MAP, graphics::cubemaps->at(cubemap));
    glDrawArrays(GL_TRIANGLES, 0, 36);
    glBindVertexArray(0);

}